package es.sidelab.animalshelter;

public class Graph {
	
	private String month;
	private int count;
	
	public Graph(String month, int count) {
		this.month = month;
		this.count = count;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	

}
